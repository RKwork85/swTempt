import random

def random_select(lst, num):
    """
    从列表中随机返回指定数量的元素。

    参数:
        lst (list): 源列表
        num (int): 需要随机挑选的元素数量

    返回:
        list: 随机挑选的元素列表
    """
    if not isinstance(lst, list):
        raise TypeError("第一个参数必须是一个列表")
    if not isinstance(num, int):
        raise TypeError("第二个参数必须是一个整数")
    if num > len(lst):
        raise ValueError("挑选的数量不能大于列表的长度")
    if num < 0:
        raise ValueError("挑选的数量不能为负数")

    return random.sample(lst, num)



